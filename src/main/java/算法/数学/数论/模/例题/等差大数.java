package 算法.数学.数论.模.例题;

public class 等差大数 {
    /*
    等差数列 1,5,9,13,...,2017
    组合成一个大数15913...2017
    求该数模9的结果
     */
    /*
    原数 = 1 * 10^? + 5 * 10^? + ... + 2013 * 10^4 + 2017
    由 10^n mod 9 = 1
    => k * 10^n mod 9 = k
    则 原数 mod 9
    = (1 + 5 + 9 + ... + 2017) mod 9
    = (1+2017)*((2017-1)/4+1)/2 mod 9
    = 2018 * 505 / 2 mod 9
    = 1009 * 505 mod 9
    = (1009 mod 9) * (505 mod 9) mod 9
    = 1 * 1 mod 9
    = 1
     */
}
