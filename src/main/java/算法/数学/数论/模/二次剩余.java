package 算法.数学.数论.模;

public class 二次剩余 {
    /*
    二次剩余:
    对于奇素数p,a与p互质
    如果存在x^k = a (mod p)
    则a为p的k次剩余

    (1) a是p的二次剩余,当且仅当 a^((p-1)/2) = 1 (mod p)
        a是p的二次非剩余,当且仅当 a^((p-1)/2) = -1 (mod p)
    (2) a^((p-1)/2) = a/p (mod p)
    (3) a1 = a2 (mod p)  等价于 a1/p = a2/p
    (4) ab^2 / p = a/p
     */

}
